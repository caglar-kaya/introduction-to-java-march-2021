package com.java.interviewQuestions;

public class Q_002_Quotinent_Remainder {

	public static void main(String[] args) {

		// Quotient = /
		// Remainder = %
		
		double num1 = 100, num2 = 3;
		
		// Quotient
		System.out.println(num1 / num2); // 33.333333333333336
		
		// Remainder
		System.out.println(num1 % num2); // 1.0
		
	}

}
